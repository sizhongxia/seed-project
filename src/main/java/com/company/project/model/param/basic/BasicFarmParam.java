package com.company.project.model.param.basic;

public class BasicFarmParam {
	private String farmId;
	private String farmName;
	private String ownerUserId;
	private String[] farmArea;
	private String farmAddress;
	private String longitude;
	private String latitude;
	private String website;
	private String organizeId;
	private String organizeName;
	private String farmRemark;

	public String getFarmId() {
		return farmId;
	}

	public void setFarmId(String farmId) {
		this.farmId = farmId;
	}

	public String getFarmName() {
		return farmName;
	}

	public void setFarmName(String farmName) {
		this.farmName = farmName;
	}

	public String getOwnerUserId() {
		return ownerUserId;
	}

	public void setOwnerUserId(String ownerUserId) {
		this.ownerUserId = ownerUserId;
	}

	public String[] getFarmArea() {
		return farmArea;
	}

	public void setFarmArea(String[] farmArea) {
		this.farmArea = farmArea;
	}

	public String getFarmAddress() {
		return farmAddress;
	}

	public void setFarmAddress(String farmAddress) {
		this.farmAddress = farmAddress;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getWebsite() {
		return website;
	}

	public void setWebsite(String website) {
		this.website = website;
	}

	public String getOrganizeId() {
		return organizeId;
	}

	public void setOrganizeId(String organizeId) {
		this.organizeId = organizeId;
	}

	public String getOrganizeName() {
		return organizeName;
	}

	public void setOrganizeName(String organizeName) {
		this.organizeName = organizeName;
	}

	public String getFarmRemark() {
		return farmRemark;
	}

	public void setFarmRemark(String farmRemark) {
		this.farmRemark = farmRemark;
	}

}
